package ink.anh.lingo;

/**
 * Class containing constants for permission strings used in the AnhyLingo plugin.
 * These constants represent different permissions that can be assigned to players or roles.
 * Each permission allows access to different features or commands within the plugin.
 */
public class Permissions {

    /**
     * Permission to access Lingo files "anhylingo.file.lingo".
     */
    public static final String FILE_LINGO = "anhylingo.file.lingo";

    /**
     * Permission to access other files "anhylingo.file.other".
     */
    public static final String FILE_OTHER = "anhylingo.file.other";

    /**
     * Permission to delete files "anhylingo.file.delete".
     */
    public static final String FILE_DELETE = "anhylingo.file.delete";

    /**
     * Permission to view directories "anhylingo.file.view".
     */
    public static final String DIR_VIEW = "anhylingo.file.view";

    /**
     * Permission to access information about items "anhylingo.items.info".
     */
    public static final String ITEMS_INFO = "anhylingo.items.info";

    /**
     * Permission to reload the plugin or its components "anhylingo.reload".
     */
    public static final String RELOAD = "anhylingo.reload";

    /**
     * Permission to set NBT (Named Binary Tag) data "anhylingo.nbt.set".
     */
    public static final String NBT_SET = "anhylingo.nbt.set";

    /**
     * Permission to list NBT data "anhylingo.nbt.list".
     */
    public static final String NBT_LIST = "anhylingo.nbt.list";

    /**
     * Permission to view information about NBT data "anhylingo.nbt.info".
     */
    public static final String NBT_INFO = "anhylingo.nbt.info";
}
